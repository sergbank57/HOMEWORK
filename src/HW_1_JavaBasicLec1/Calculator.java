package HW_1_JavaBasicLec1;

public class Calculator {
    public double sum(double x, double y) {
        double resultsum = x + y;
        return resultsum;
    }

    public double subm(double x, double y) {
        double resultsubm = x - y;
        return resultsubm;
    }

    public double multip(double x, double y) {
        double resultmultip = x * y;
        return resultmultip;
    }

    public double divis(double x, double y) {
        double resultdivis = x / y;
        return resultdivis;
    }
}

